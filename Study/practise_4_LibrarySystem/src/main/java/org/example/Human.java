package org.example;

public class Human {
    public int id;
    public String name;
    public String surname;
    public String middleName;
    public String address;

    public Human(int id, String name, String surname, String middleName, String address)
    {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.middleName = middleName;
        this.address = address;
    }
}
